# Integers

4 + 2
4 - 2
4 / 2

# Standard math rules: PEMDAS
x = (4 + 2) - 3
x += 2 # same as: x = x + 2
x + 2 # different from above, this does not modify variable x

# Tip: calling .class tells us what class that object belongs to
x = 1
x.class # integer

y = 1.1
y.class # float

100.class # integer

10 / 3 # 3

10.0 / 3 # 3.33333333
10 / 3.0 # same as above

# useful methods
10.to_f # convert to float
3.0.to_i # convert to integer